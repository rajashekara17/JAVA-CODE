class PremitiveDatatypes{
	public static void main(String[]args){
		byte age=117;
		short salary=22000;
		int marks=99;
		long phone=8105183948l;
		float percentage=80.20f;
		double value=110.0000d;
		boolean java = true;
		char Name = 'A';
		System.out.println( "age:"  + age +  "salary:"  + salary + "marks:" + marks + "phone:" + phone + "percentage:" + percentage + "value:" + value + "java:" + java + "Name:" + Name );
	}
	
}
	